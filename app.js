const arrOne = document.querySelector(".one");
const ansOne = document.querySelector(".one-a");

var flag = 0

arrOne.addEventListener("click", function(){
    if(flag == 0){
        ansOne.style.display = "block";
        flag = 1
    } else {
        ansOne.style.display = "none";
        flag = 0
    }
    
})

